			<h4><font color="red">What is DRP?</font></h4>
			The DRP offers a cross-disciplinary program of professional enrichment. <br>
			The program consists of an introductory course on problems and approaches in education research followed by a set of modular course offerings focused on professional development.
			<br><br>

			<h4><font color="red">Who can be a DRP fellow? </font></h4>
			All School of Education Graduate students interested in research.<br><br>



			<h4><font color="red">Why should I be a part of DRP?</font></h4>
			Students will get publication opportunities, impactful community experiences, chance for networking and sponsored conference travel.<br><br>


